import java.util.Scanner;

public class SquareNum {
    public static void main(String[] args) {
        int n, area;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter any number :");
        n=sc.nextInt();
        area=n*n;
        System.out.println("Area of Square : " +area);

    }
    
}
